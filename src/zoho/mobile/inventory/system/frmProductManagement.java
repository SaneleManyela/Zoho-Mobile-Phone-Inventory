    String[] arrFileContents;
    
    //A method that checks if the provided quantity is correct then
    //returns a value of type bolooen depending on the evaluation
    private boolean mCheckQuantityCorrectness()
    {
        boolean isCorrect = false;
        try{
            isCorrect = Integer.valueOf(txtQuantity.getText()) > 0 
                    && Integer.valueOf(txtQuantity.getText()) < Integer.MAX_VALUE;
        }catch(NumberFormatException eX){
            JOptionPane.showMessageDialog(null, txtQuantity.getText()+" is not an integer number");
            txtQuantity.requestFocusInWindow();
        }
        return isCorrect;
    }
    
    //A method that gets GUI entered values, concatenate and assign them to
    //variable arrFileContents.
    private void mGetValuesFromGUI()
    {
        arrFileContents = new String[]{
            txtProductName.getText(), txtQuantity.getText(),
            txtDescription.getText()
        };
    }
    
    //A method that clears GUI text fields.
    private void mClear()
    {
        txtProductName.setText("");
        txtQuantity.setText("");
        txtDescription.setText("");
    }
    
    //A method that checks if an accountant file already exits and
    //delete it if it does then create a new file. Whether a file 
    //exists or not, a new file is created either way.
    private void mCheckIfProductFileExists()
    {
        flProduct = new File(txtProductName.getText().toUpperCase()+".txt");
        if(flProduct.exists())
        {
            flProduct.delete();
        }
        mCreateProductFile();
    }
    
    //A method that creates a new file using the File variable flProduct.
    private void mCreateProductFile()
    {
        try
        {
            flProduct.createNewFile();
        }
        catch(IOException eX)
        {
            JOptionPane.showMessageDialog(null, "Something went wrong while creating file.\n"+eX.getMessage());
        }
    }
    
    //A method that writes to the newly created file
    private void mWriteProductDetailsToFile()
    {
        String strProduct = txtProductName.getText().toUpperCase()+".txt";
        try
        {
            try (java.io.FileWriter fw = new java.io.FileWriter(strProduct, true)) {
                for (String productDetails : arrFileContents) {
                    fw.write(productDetails+" ");
                }
                fw.close();
            }
            JOptionPane.showMessageDialog(null, "File added successfully.");
        }
        catch(IOException eX)
        {
            JOptionPane.showMessageDialog(null, "Something went wrong while writing file.\n"+eX.getMessage());
        }
    }
    
