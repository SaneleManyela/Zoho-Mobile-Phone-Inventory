    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtProductName.getText().equals("")){//Checks if txtProductName has been passed a value
            JOptionPane.showMessageDialog(null, "Please enter product name.");
            txtProductName.requestFocusInWindow();//Places the cursor inside txtProductName
        }else if(txtQuantity.getText().equals("")){//Checks if txtQuantity has been passed a value
            JOptionPane.showMessageDialog(null, "Please enter product quantity.");
            txtQuantity.requestFocusInWindow();//Places the cursor inside txtQuantity
        }else if(txtDescription.getText().equals("")){//Checks if txtDescription has been passed a value
            JOptionPane.showMessageDialog(null, "Please enter product description.");
            txtDescription.requestFocusInWindow();//Places the cursor inside txtDescription
        }else if(mCheckQuantityCorrectness()){//Checks for the correctness of given qantity value
            mGetValuesFromGUI();
            mCheckIfProductFileExists();
            mWriteProductDetailsToFile();
            mClear();
        }else{//if given quantity is not correct the follow message box pops up
            JOptionPane.showMessageDialog(null, "Product quantity must be any integer number between 1 and "+Integer.MAX_VALUE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

